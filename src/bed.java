    private DefaultListModel<String> getList(int room) throws SQLException{
        DefaultListModel<String> items = new DefaultListModel<>();
        pst = con.prepareStatement("SELECT p.name FROM bed AS b INNER JOIN patient AS p ON p.patientno = b.patient_no  WHERE room_no = ?");
        pst.setInt(1,room);
        rs = pst.executeQuery();
        while (rs.next())
            items.addElement(rs.getString("name"));
        
        return items;
    }
